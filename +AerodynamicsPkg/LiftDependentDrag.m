function [FCDP] = EdgeInterp(A1, A2, FCDP1, FCDP2, A)
%
% [FCDP] = EdgeInterp(A1, A2, FCDP1, FCDP2, A)
% modified by Paul Mokotoff, prmoko@umich.edu
% patterned after the "edge_interp" method from Aviary's
% "lift_dependent_drag.py", translated by Cursor, an AI Code Editor
% last updated: 27 may 2025
%
% interpolate along an edge.
%
% INPUTS:
%     A1    - left A    value
%     A2    - rite A    value
%     FCDP1 - left FCDP value
%     FCDP2 - rite FCDP value
%     A     - current A value for interpolation
%
% OUTPUTS:
%     FCDP  - the interpolated FCDP value
%

% compute the denominator
den = 1.0 / ((A - A1) * FCDP1 - (A - A2) * FCDP2);

% interpolate
FCDP = 2.0 * FCDP1 * FCDP2 * den;

end

