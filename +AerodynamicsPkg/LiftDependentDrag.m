function [CDi] = LiftDependentDrag(Inputs)
%
% [CDi] = LiftDependentDrag(Inputs)
% modified by Paul Mokotoff, prmoko@umich.edu
% patterned after Aviary's "compute" method in lift_dependent_drag.py,
% translated by Cursor, an AI Code Editor
% last updated: 28 may 2025
%
% compute the lift dependent drag for a configuration.
%
% INPUTS:
%     Inputs - data structure with all necessary inputs.
%              size/type/units: 1-by-1 / struct / []
%
% OUTPUTS:
%     CDi    - induced drag coefficient due to lift.
%              size/type/units: npnt-by-1 / double / []
%


%% INITIALIZATION %%
%%%%%%%%%%%%%%%%%%%%

% get the inputs
Mach = Inputs.Mach;
L = Inputs.Lift;
P = Inputs.Pressure;
CL = Inputs.DesignLiftCoefficient;
DesignMach = Inputs.DesignMach;
Swing = Inputs.WingArea;
AR = Inputs.AspectRatio;
MaxCamber = Inputs.MaxCamber;
T_C = Inputs.ThicknessChord;
nnodes = Inputs.NumNodes;
Gamma = Inputs.Gamma;

% allocate memory for drag coefficient
FCDP = zeros(nnodes, 1);


%% COMPUTE THE LIFT DEPENDENT DRAG %%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

% calculate wing lift coefficient
CLwing = 2.0 * L ./ (Swing * Gamma * P .* Mach.^2);

% calculate differences relative to the design conditions
dCL = CLwing - CL;
dM  = Mach - DesignMach;
A   = AR * T_C ^ (1.0/3.0);

% get the tables for processing all nodes
[AR05, AR1, AR2, AR4, AR6, ARS07, ARS08, ARS10, ARS12, ARS14, ARS16, ARS18, ARS20] = SetupTables();

% process each node
for i = 1:nnodes
    
    % get the current delta
    x = [dM(i), dCL(i)];
    
    % find the appropriate interpolation based on mach number change
    if (dM(i) <= 0.075)
        
        % find the appropriate interpolation based on A
        if (A < 0.5)
            
            % get the factors from the interpolation tables
            FCDP1 = AR05(x);
            FCDP2 = AR1(x);
            
            % remember the A values
            A1 = 0.5;
            A2 = 1.0;
            
            % interpolate along the edge
            FCDP(i) = EdgeInterp(A1, A2, FCDP1, FCDP2, A);
            
        elseif ((A >= 0.5) && (A < 6))
            
            % get the factors from the interpolation tables
            FCDP1 = AR05(x);
            FCDP2 = AR1(x);
            FCDP3 = AR2(x);
            FCDP4 = AR4(x);
            FCDP5 = AR6(x);
            
            % remember the A values
            arrA = [0.5, 1, 2, 4, 6];
            
            % interpolate along the interior
            FCDP(i) = InnerInterp(arrA, [FCDP1, FCDP2, FCDP3, FCDP4, FCDP5], A);
            
        else
            
            % get the factors from the interpolation tables
            FCDP1 = AR4(x);
            FCDP2 = AR6(x);
            
            % remember the A values
            A1 = 4.0;
            A2 = 6.0;
            
            % interpolate along the edge
            FCDP(i) = EdgeInterp(A1, A2, FCDP1, FCDP2, A);
            
        end
        
    else
        
        % find the appropriate interpolation based on A
        if (A < 0.7)
            
            % get the factors from the interpolation tables
            FCDP1 = ARS07(x);
            FCDP2 = ARS08(x);
            
            % remember the A values
            A1 = 0.7;
            A2 = 0.8;
            
            % interpolate along the edge
            FCDP(i) = EdgeInterp(A1, A2, FCDP1, FCDP2, A);
            
        elseif ((A >= 0.7) && (A <= 1.4))
            
            % get the factors from the interpolation tables
            FCDP1 = ARS07(x);
            FCDP2 = ARS08(x);
            FCDP3 = ARS10(x);
            FCDP4 = ARS12(x);
            FCDP5 = ARS14(x);
            
            % remember the A values
            arrA = [0.7, 0.8, 1.0, 1.2, 1.4];
            
            % interpolate along the interior
            FCDP(i) = InnerInterp(arrA, [FCDP1, FCDP2, FCDP3, FCDP4, FCDP5], A);
            
        elseif ((A > 1.4) && (A <= 2.0))
            
            % get the factors from the interpolation table
            FCDP1 = ARS12(x);
            FCDP2 = ARS14(x);
            FCDP3 = ARS16(x);
            FCDP4 = ARS18(x);
            FCDP5 = ARS20(x);
            
            % remember the A values
            arrA = [1.2, 1.4, 1.6, 1.8, 2.0];
            
            % interpolate along the interior
            FCDP(i) = InnerInterp(arrA, [FCDP1, FCDP2, FCDP3, FCDP4, FCDP5], A);
            
        else
            
            % get the factors from the interpolation table
            FCDP1 = ARS18(x);
            FCDP2 = ARS20(x);
            
            % remember the A values
            A1 = 1.8;
            A2 = 2.0;
            
            % interpolate along the edge
            FCDP(i) = EdgeInterp(A1, A2, FCDP1, FCDP2, A);
            
        end
    end
end

% compute the final drag coefficient
DCDP = FCDP .* (1.0 + MaxCamber/10.0) .* A ./ AR;

% if any are less than zero, reset them to 0
DCDP(DCDP < 0) = 0.0;

% set the final outputs
CDi = DCDP;


end

