function [FCDPOut] = InnerInterp(A, FCDP, CurA)
%
% [FCDP] = InnerInterp(arrA, arrFCDP, A)
% modified by Paul Mokotoff, prmoko@umich.edu
% patterned after the "inner_interp" method from Aviary's
% "lift_dependent_drag.py", translated by Cursor, an AI Code Editor
% last updated: 27 may 2025
%
% use a linear interpolation to interpolate along the interior.
%
% INPUTS:
%     A       - values of A.
%               size/type/units: n-by-1 / double / []
%
%     FCDP    - values of FCDP.
%               size/type/units: n-by-1 / double / []
%
%     CurA    - current A value.
%               size/type/units: 1-by-1 / double / []
%
% OUTPUTS:
%     FCDPOut - interpolated FCDP value.
%               size/type/units: 1-by-1 / double / []
%

% interpolate
FCDPOut = interp1(A, FCDP, CurA, 'pchip', 'extrap');

end

