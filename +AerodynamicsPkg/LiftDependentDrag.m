function CD = LiftDependentDrag(Mach, Lift, P, ClDes, MDes, Sref, AR, Cam, TC, Gamma)
% COMPUTELIFTDEPENDENTDRAG Calculates lift-dependent drag
% Inputs:
%   Mach - Mach number
%   Lift - Lift force (lbf)
%   P - Static pressure (lbf/ft^2)
%   ClDes - Design lift coefficient
%   MDes - Design Mach number
%   Sref - Wing reference area (ft^2)
%   AR - Wing aspect ratio
%   Cam - Wing max camber at 70% semispan
%   SW25 - Wing sweep (deg)
%   TC - Wing thickness-to-chord ratio
%   Gamma - Ratio of specific heats (default = 1.4)
%
% Outputs:
%   CD - Coefficient of lift dependent drag

if nargin < 11
    Gamma = 1.4;
end

% Calculate number of nodes
Nn = length(Mach);

% Initialize arrays
Fcdp = zeros(Nn, 1);
Dcdp = zeros(Nn, 1);
DFcdpDDelM = zeros(Nn, 1);
DFcdpDDelCl = zeros(Nn, 1);
DFcdpDA = zeros(Nn, 1);

% Calculate CL and deltas
Cl = 2.0 * Lift ./ (Sref * Gamma * P .* Mach.^2);
DelCl = Cl - ClDes;
DelM = Mach - MDes;
A = AR * TC^(1.0/3.0);

% Process each node
for i = 1:Nn
    x = [DelM(i), DelCl(i)];
    
    if DelM(i) <= 0.075
        if A < 0.5
            % Edge interpolation for A < 0.5
            Fcdp1 = interp2(Ar05X, Ar05Y, Ar05Table, x(1), x(2));
            Fcdp2 = interp2(Ar1X, Ar1Y, Ar1Table, x(1), x(2));
            
            A1 = 0.5;
            A2 = 1.0;
            [Fcdp(i), DFcdpDDelM(i), DFcdpDDelCl(i), DFcdpDA(i)] = EdgeInterp(A1, A2, Fcdp1, Fcdp2, A);
            
        elseif A >= 0.5 && A < 6
            % Inner interpolation for 0.5 <= A < 6
            Fcdp1 = interp2(Ar05X, Ar05Y, Ar05Table, x(1), x(2));
            Fcdp2 = interp2(Ar1X, Ar1Y, Ar1Table, x(1), x(2));
            Fcdp3 = interp2(Ar2X, Ar2Y, Ar2Table, x(1), x(2));
            Fcdp4 = interp2(Ar4X, Ar4Y, Ar4Table, x(1), x(2));
            Fcdp5 = interp2(Ar6X, Ar6Y, Ar6Table, x(1), x(2));
            
            ArrA = [0.5, 1, 2, 4, 6];
            [Fcdp(i), DFcdpDDelM(i), DFcdpDDelCl(i), DFcdpDA(i)] = InnerInterp(ArrA, Fcdp1, Fcdp2, Fcdp3, Fcdp4, Fcdp5, A);
            
        else
            % Edge interpolation for A >= 6
            Fcdp1 = interp2(Ar4X, Ar4Y, Ar4Table, x(1), x(2));
            Fcdp2 = interp2(Ar6X, Ar6Y, Ar6Table, x(1), x(2));
            
            A1 = 4.0;
            A2 = 6.0;
            [Fcdp(i), DFcdpDDelM(i), DFcdpDDelCl(i), DFcdpDA(i)] = EdgeInterp(A1, A2, Fcdp1, Fcdp2, A);
        end
        
    else
        if A < 0.7
            % Edge interpolation for A < 0.7
            Fcdp1 = interp2(Ars07X, Ars07Y, Ars07Table, x(1), x(2));
            Fcdp2 = interp2(Ars08X, Ars08Y, Ars08Table, x(1), x(2));
            
            A1 = 0.7;
            A2 = 0.8;
            [Fcdp(i), DFcdpDDelM(i), DFcdpDDelCl(i), DFcdpDA(i)] = EdgeInterp(A1, A2, Fcdp1, Fcdp2, A);
            
        elseif A >= 0.7 && A <= 1.4
            % Inner interpolation for 0.7 <= A <= 1.4
            Fcdp1 = interp2(Ars07X, Ars07Y, Ars07Table, x(1), x(2));
            Fcdp2 = interp2(Ars08X, Ars08Y, Ars08Table, x(1), x(2));
            Fcdp3 = interp2(Ars10X, Ars10Y, Ars10Table, x(1), x(2));
            Fcdp4 = interp2(Ars12X, Ars12Y, Ars12Table, x(1), x(2));
            Fcdp5 = interp2(Ars14X, Ars14Y, Ars14Table, x(1), x(2));
            
            ArrA = [0.7, 0.8, 1.0, 1.2, 1.4];
            [Fcdp(i), DFcdpDDelM(i), DFcdpDDelCl(i), DFcdpDA(i)] = InnerInterp(ArrA, Fcdp1, Fcdp2, Fcdp3, Fcdp4, Fcdp5, A);
            
        elseif A > 1.4 && A <= 2.0
            % Inner interpolation for 1.4 < A <= 2.0
            Fcdp1 = interp2(Ars12X, Ars12Y, Ars12Table, x(1), x(2));
            Fcdp2 = interp2(Ars14X, Ars14Y, Ars14Table, x(1), x(2));
            Fcdp3 = interp2(Ars16X, Ars16Y, Ars16Table, x(1), x(2));
            Fcdp4 = interp2(Ars18X, Ars18Y, Ars18Table, x(1), x(2));
            Fcdp5 = interp2(Ars20X, Ars20Y, Ars20Table, x(1), x(2));
            
            ArrA = [1.2, 1.4, 1.6, 1.8, 2.0];
            [Fcdp(i), DFcdpDDelM(i), DFcdpDDelCl(i), DFcdpDA(i)] = InnerInterp(ArrA, Fcdp1, Fcdp2, Fcdp3, Fcdp4, Fcdp5, A);
            
        else
            % Edge interpolation for A > 2.0
            Fcdp1 = interp2(Ars18X, Ars18Y, Ars18Table, x(1), x(2));
            Fcdp2 = interp2(Ars20X, Ars20Y, Ars20Table, x(1), x(2));
            
            A1 = 1.8;
            A2 = 2.0;
            [Fcdp(i), DFcdpDDelM(i), DFcdpDDelCl(i), DFcdpDA(i)] = EdgeInterp(A1, A2, Fcdp1, Fcdp2, A);
        end
    end
end

% Calculate final drag coefficient
Dcdp = Fcdp .* (1.0 + Cam/10.0) .* A/AR;

% Clamp negative values to zero
Dcdp(Dcdp < 0) = 0.0;

CD = Dcdp;
end

function [FCDP, dFCDP_dDEL1, dFCDP_dDEL2, dFCDP_dA] = EdgeInterp(A1, A2, FCDP1, FCDP2, dFCDP1, dFCDP2, A)
%EDGE_INTERP Performs edge interpolation between two points
%   A1, A2: Aspect ratio points
%   FCDP1, FCDP2: Function values at A1 and A2
%   dFCDP1, dFCDP2: Derivatives at A1 and A2
%   A: Current aspect ratio value

% Calculate denominator
den = 1.0 / ((A - A1) * FCDP1 - (A - A2) * FCDP2);

% Calculate interpolated value
FCDP = 2.0 * FCDP1 * FCDP2 * den;

% Calculate derivative with respect to A
dFCDP_dA = 2.0 * FCDP1 * FCDP2 * (FCDP2 - FCDP1) * den^2;

% Calculate derivatives with respect to DEL variables
dFCDP_dDEL = 2.0 * den * (dFCDP1 * (FCDP2 - FCDP1 * FCDP2 * den * (A - A1)) + ...
                          dFCDP2 * (FCDP1 + FCDP1 * FCDP2 * den * (A - A2)));

% Extract individual derivatives
dFCDP_dDEL1 = dFCDP_dDEL(:, 1);
dFCDP_dDEL2 = dFCDP_dDEL(:, 2);

end

function [FCDP, dFCDP_dDELM, dFCDP_dDELCL, dFCDP_dA] = InnerInterp(arrA, FCDP1, FCDP2, FCDP3, FCDP4, FCDP5, dFCDP1, dFCDP2, dFCDP3, dFCDP4, dFCDP5, A)
%INNER_INTERP Performs interpolation between multiple points using Lagrange interpolation
%   arrA: Array of aspect ratio points
%   FCDP1-5: Function values at each point
%   dFCDP1-5: Derivatives at each point
%   A: Current aspect ratio value

% Reshape function values into column vector
arrFCDP = [FCDP1; FCDP2; FCDP3; FCDP4; FCDP5];

% Interpolate function value and get derivative
[FCDP, deriv] = interp1(arrA, arrFCDP, A, 'pchip', 'extrap');
dFCDP_dA = deriv;

% Reshape DELM derivatives into column vector
arrdFCDP_dDELM = [dFCDP1(:,1); dFCDP2(:,1); dFCDP3(:,1); dFCDP4(:,1); dFCDP5(:,1)];

% Interpolate DELM derivative
[dFCDP_dDELM, ~] = interp1(arrA, arrdFCDP_dDELM, A, 'pchip', 'extrap');

% Reshape DELCL derivatives into column vector
arrdFCDP_dDELCL = [dFCDP1(:,2); dFCDP2(:,2); dFCDP3(:,2); dFCDP4(:,2); dFCDP5(:,2)];

% Interpolate DELCL derivative
[dFCDP_dDELCL, ~] = interp1(arrA, arrdFCDP_dDELCL, A, 'pchip', 'extrap');

end

